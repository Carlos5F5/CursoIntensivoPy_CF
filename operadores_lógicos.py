num1 = int(input("Escribe un número: "))
num2 = int(input("Escribe otro número: "))

print("Si los numeros son iguales es verdadero porque uso ==", num1 == num2) # True si son iguales, False si no lo son
print("Si los numeros son diferentes es verdadero porque uso !=", num1 != num2) # True si son diferentes, False si son iguales
print("Si el numero 1 es mayor que el 2 es verdadero porque uso > ", num1 > num2) # True si num1 es mayor que num2, False si no lo es
print("Si el numero 1 es menor que el 2 es verdadero porque uso < ", num1 < num2) # True si num1 es menor que num2, False si no lo es

